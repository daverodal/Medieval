?><span class="big">MOVEMENT</span>

<p>During a player’s Movement phase he may move some none or all of his units subject to the following restrictions.
    Units are moved one at a time with the player concluding all movement for one unit before moving on to the next.
    A Unit must stop its movement upon entering a Hostile Zone of Control
</p>

<p>UUnits Move from the hex they are in to a contiguous frontal hex. (See Unit Facing above).
</p>
<ol>
    <li><span class="big">Unit Movement Factor
</span><p>
            Each unit has a Movement Factor expressed as a number of points.
            These points are expended as the unit moves into new hexes and across certain types of hex sides.
            The cost of entering each type of hex or crossing hex sides is found on the Terrain Effects Chart.
            A unit’s movement may further be effected by the units Status (See Unit Status above) and proximity to its Leader (See Leaders above.)
            Once a unit has expended all of its movement points it must stop. It may stop at any time and need not use all of its movement points.

        </p>
    </li>
    <li><span class="big">Changing Facing</span>
        <p>
            As well as moving a unit may change its facing during movement.  It costs one movement point to change facing from the unit’s current center frontal hex side to one of its adjacent frontal hex sides; or the hex side 180 degrees opposite of its current center frontal hex side.
            A unit may combine facing changes and movement as long as it has movement points to expend.
        </p>
    </li>
    <li><span class="big">Back Step</span>
    <p>
        A unit may expend ALL of its movement points. To move directly backward into one of its rear hexes without making a facing change.
    </p>
    </li>
    <li><span class="big">Backing Off  (Exiting Zones of Control)</span>
        <p>
            Only Cavalry and Skirmishers may exit a hostile zone of control and then only if the hostile unit exerting the zone of control is infantry.
            The unit exiting the zone of control may not enter another hostile zone of control during that movement phase.
        </p>
    </li>
</ol>